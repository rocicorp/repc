    fn find_by_sum() {
        const PHRASE1: &str = "Aenean massa. Cum sociis natoque";
        const PHRASE2: &str = "Phasellus leo dolor, tempus non, auctor et, hendrerit quis, nisi";
        let p1idx = LOREM_IPSUM_1.find(PHRASE1).unwrap() as isize;
        let p2idx = LOREM_IPSUM_2.find(PHRASE2).unwrap() as isize;

        let mut h1 = BuzHash::new(32);
        write!(h1, "{}", PHRASE1).expect("write! failed");
        let p1sum = h1.sum();

        h1.reset();
        for (i, b) in LOREM_IPSUM_1.as_bytes().iter().enumerate() {
            let i = i as isize;
            h1.hash_byte(*b);
            assert_eq!(p1idx == i - PHRASE1.len() as isize + 1, h1.sum() == p1sum);
        }

        let mut h2 = BuzHash::new(64);
        write!(h2, "{}", PHRASE2).expect("write! failed");
        let p2sum = h2.sum();

        h2.reset();
        for (i, b) in LOREM_IPSUM_2.as_bytes().iter().enumerate() {
            let i = i as isize;
            h2.hash_byte(*b);
            assert_eq!(p2idx == i - PHRASE2.len() as isize + 1, h2.sum() == p2sum);
        }
    }

