pub struct BuzHash {
    state: u32,
    buf: Vec<u8>,
    bshiftn: u32,
    bshiftm: u32,
    pos: u32,
    overflow: bool,
}

#[allow(dead_code)]
impl BuzHash {
    pub fn new(n: u32) -> BuzHash {
        let bshiftn = n % 32;
        BuzHash {
            state: 0,
            buf: vec![0; n as usize],
            bshiftn,
            bshiftm: 32 - bshiftn,
            pos: 0,
            overflow: false,
        }
    }

    pub fn hash_byte(&mut self, b: u8) {
        if self.pos == self.buf.capacity() as u32 {
            self.overflow = true;
            self.pos = 0;
        }

        let mut s = self.state;
        s = (s.rotate_left(1)) | (s.rotate_right(31));

        if self.overflow {
            let toshift = BUZHASH_TABLE[self.buf[self.pos as usize] as usize];
            s ^= (toshift.rotate_left(self.bshiftn)) | (toshift.rotate_right(self.bshiftm));
        }

        self.buf[self.pos as usize] = b;
        self.pos += 1;

        s ^= BUZHASH_TABLE[b as usize];
        self.state = s;
    }

    pub fn sum(&self) -> u32 {
        self.state
    }

    pub fn reset(&mut self) {
        self.state = 0;
        self.pos = 0;
        self.overflow = false;
    }
}

impl Write for BuzHash {
    fn write(&mut self, buf: &[u8]) -> Result<usize> {
        for b in buf {
            self.hash_byte(*b);
        }
        Ok(buf.len())
    }

    fn flush(&mut self) -> Result<()> {
        Ok(())
    }
}

